    private boolean cadastroValido(){
        if(jTextField1.getText().trim().length() == 0){
            jLabel4.setText("CAMPOS OBRIGATÃ“RIOS");
            jLabel4.setEnabled(true);
            return false;
        }
        return true;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean efetuou = false;
        if(cadastroValido()){
            Especie especie = new Especie();
            especie.setNome(jTextField1.getText().toUpperCase());
            EspecieAppService especieAppService = (EspecieAppService) fabrica.getBean("especieAppService");
            especieAppService.inclui(especie);
            efetuou = true;
        }
        if(efetuou){
            jLabel4.setText("CADASTRO REALIZADO");
            jLabel4.setEnabled(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
